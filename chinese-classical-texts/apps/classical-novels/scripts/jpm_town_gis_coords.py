GIS_BOUNDS = {"width": 1400, "height": 900}

ZONE_HULLS: dict[str, list[list[int]]] = {
    "府内": [[680, 395], [760, 395], [760, 465], [680, 465]],
    "市井": [[540, 330], [820, 330], [820, 560], [540, 560]],
    "寺观": [[630, 335], [710, 335], [720, 575], [630, 575]],
    "城外": [[0, 0], [1400, 0], [1400, 900], [0, 900]],
}


def apply_gis_coords(nodes: list[dict]) -> None:
    missing: list[str] = []
    for n in nodes:
        entry = GIS_COORDS.get(n["id"])
        if not entry:
            missing.append(n["id"])
            continue
        n["coord"] = (entry[0], entry[1])
    if missing:
        raise SystemExit(f"GIS_COORDS missing: {', '.join(missing)}")


def write_json() -> None:
    payload = {
        "book": "金瓶梅",
        "crs": "simple",
        "bounds": {"width": GIS_BOUNDS["width"], "height": GIS_BOUNDS["height"]},
        "axis": {"x": "东", "y": "南"},
        "scale_note": (
            "清河县城示意 GIS（Leaflet Simple CRS）。"
            "方位据词话本街巷叙述与《西门府建筑名录》动线推断，"
            "宏观比例参考侯会清河复原、白维国宅院图（外部·待核），非测绘坐标。"
        ),
        "zone_hulls": ZONE_HULLS,
        "nodes": {
            k: {"x": v[0], "y": v[1], "source": v[2]} for k, v in GIS_COORDS.items()
        },
    }
    OUT_JSON.write_text(json.dumps(payload, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")
    print(f"wrote {OUT_JSON.relative_to(ROOT)} ({len(GIS_COORDS)} nodes)")


if __name__ == "__main__":
    import sys

    if "--write" in sys.argv:
        write_json()
    else:
        print(f"{len(GIS_COORDS)} coords; run with --write to export JSON")
