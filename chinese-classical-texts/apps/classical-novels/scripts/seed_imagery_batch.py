"""seed_imagery_batch.py — 批量补全 西游记/金瓶梅 诗词意象（imagery）。

可重复运行：已存在的 <id>.md 不覆盖（除非 --force）。
数据见下方 XIYOU / JINPING 列表，每条 = imagery collection 一页。
"""
from __future__ import annotations
import argparse
from pathlib import Path

ROOT = Path(__file__).resolve().parents[1]
OUT = ROOT / "src" / "content" / "imagery"


def emit(book: str, entries: list[dict], force: bool) -> tuple[int, int]:
    """写出一本书的 imagery 文件，返回 (新建, 跳过)。"""
    book_dir = OUT / book
    book_dir.mkdir(parents=True, exist_ok=True)
    created = skipped = 0
    for e in entries:
        fp = book_dir / f"{e['id']}.md"
        if fp.exists() and not force:
            skipped += 1
            continue
        fp.write_text(render(book, e), encoding="utf-8")
        created += 1
    return created, skipped


def render(book: str, e: dict) -> str:
    L = ["---", f"id: {e['id']}", "type: imagery", f"book: {book}",
         f"subtype: {e['subtype']}", f"title: {e['title']}"]
    if e.get("chapters"):
        L.append("chapters:")
        L += [f"  - {c}" for c in e["chapters"]]
    if e.get("characters"):
        L.append("characters:")
        L += [f"  - {c}" for c in e["characters"]]
    if e.get("links"):
        L.append("links:")
        for k in e["links"]:
            L.append(f"  - target: {k['target']}")
            L.append(f"    target_kind: {k.get('target_kind', 'character')}")
            L.append(f"    predicate: {k['predicate']}")
            L.append(f"    inference: {str(k.get('inference', True)).lower()}")
            if "chapter" in k:
                L.append(f"    chapter: {k['chapter']}")
            if "phase" in k:
                L.append(f"    phase: {k['phase']}")
            if "temperature" in k:
                L.append(f"    temperature: {k['temperature']}")
            if "note" in k:
                L.append(f"    note: \"{k['note']}\"")
    L.append("tags: [" + ", ".join(e["tags"]) + "]")
    L.append(f"summary: \"{e['summary']}\"")
    if e.get("source"):
        L.append(f"source: {e['source']}")
    L.append("---")
    L.append("")
    L.append(e["body"].strip())
    L.append("")
    return "\n".join(L)


