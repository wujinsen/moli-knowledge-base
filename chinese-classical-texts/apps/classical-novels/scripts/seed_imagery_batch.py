def _xp(id_, title, chapters, char, note, tags, summary, body, src=None):
    e = {"id": id_, "subtype": "place_omen", "title": title, "chapters": chapters,
         "characters": [char] if isinstance(char, str) else char,
         "links": [{"target": (char if isinstance(char, str) else char[0]),
                    "predicate": "隐喻", "inference": True,
                    "chapter": chapters[0] if chapters else None, "note": note}],
         "tags": tags, "summary": summary, "body": body}
    if e["links"][0]["chapter"] is None:
        del e["links"][0]["chapter"]
    if src:
        e["source"] = src
    return e


